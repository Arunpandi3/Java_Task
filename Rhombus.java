import java.util.Scanner;
class Rhombus{
public static void main(String args[]){
Scanner Rho = new Scanner(System.in);
System.out.println("Enter first value");
int First = Rho.nextInt();
System.out.println("Enter second value");
int Second = Rho.nextInt();
int Rombus = (First * Second) / 2;
System.out.println("The Rhombus value is : " + Rombus);
}
} 
